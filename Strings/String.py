# Ord and chr 
print(ord("a"))     # chr to ord
print(ord("A"))
print(chr(97))      # ord to chr
print(chr(65))

print(ord('z'))
print(ord('Z'))


# UNICODE for a : 97
# UNICODE for z : 122
# UNICODE for A : 65
# UNICODE for Z : 90


