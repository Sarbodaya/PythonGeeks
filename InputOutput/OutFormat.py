# Python program showing use of format method

print("I love {} for {}".format("Geeks", "Geeks!!!!"))


# using a format method to position the object
print("I love {0} for {1}".format("Geeks", "Materials"))

print("I love {1} for {0}".format("Geeks", "Materials"))

# combining positional and keywords arguments
print("Number one portal is {0} {1} {others}".format("Geeks", "For", others="Geeks"))

# using format() method with number

print("Total number of boys : {0:2d} and numbers of girls : {1:5.2f}".format(123, 233.344545))

