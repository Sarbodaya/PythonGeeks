# Examples of Arithmetic Operator
a = 9
b = 4

# Addition of numbers
add = a + b

# Subtraction of numbers
sub = a - b

# Multiplication of number
mul = a * b

# Division(float) of number
div1 = a / b

# Division(floor) of number
div2 = a // b

# Modulo of both number
mod = a % b

# Power
p = a ** b

# print results
print("ADD : ", add)
print("SUB : ", sub)
print("MUL : ", mul)
print("DIV1 : ", div1)
print("DIV2 : ", div2)
print("MOD : ", mod)
print("POWER : ", p)
