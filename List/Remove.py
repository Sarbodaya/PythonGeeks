List = [10, 20, 30, 40, 50, 60, 70, 80]
print(List)

# Remove Method
List.remove(30)
print(List)

# Pop
print(List.pop())
print(List)

# Pop With Index
print(List.pop(2))
print(List)

# Del keyword
del List[1]
print("Del Keyword : ")
print(List)

# Del with Slicing
del List[0:2]
print(List)



